import { useLocation, useNavigate } from "react-router-dom";
import {
  Brain,
  AlertTriangle,
  CheckCircle2,
  ArrowLeft,
  Download,
  ShieldCheck,
  Calendar,
  Clock,
  User,
  Cpu,
} from "lucide-react";

import jsPDF from "jspdf";

export default function Results() {
  const { state } = useLocation();
  const navigate = useNavigate();

  if (!state) {
    return (
      <div className="min-h-screen flex justify-center items-center text-2xl font-bold">
        No prediction found.
      </div>
    );
  }

  const {
    prediction,
    confidence,
    patient,
    model,
  } = state;

  const healthy =
    prediction === "Healthy Control" ||
    prediction === "C";

  const confidenceLevel =
    confidence >= 95
      ? "High Confidence"
      : confidence >= 80
      ? "Moderate Confidence"
      : "Low Confidence";

  const downloadReport = () => {

    const doc = new jsPDF();

    // -----------------------------
    // Header
    // -----------------------------

    doc.setFillColor(37, 99, 235);
    doc.rect(0,0,210,35,"F");

    doc.setTextColor(255,255,255);
    doc.setFont("helvetica","bold");
    doc.setFontSize(24);
    doc.text("NeuroScan AI",20,18);

    doc.setFontSize(13);
    doc.text("EEG Alzheimer's Disease Detection Report",20,28);

    // -----------------------------
    // Patient Details
    // -----------------------------

    doc.setTextColor(0,0,0);

    doc.setFontSize(18);
    doc.text("Patient Information",20,50);

    doc.setDrawColor(220);
    doc.roundedRect(20,58,170,40,3,3);

    doc.setFontSize(12);

    doc.text(`Patient Name : ${patient.name || "-"}`,25,70);
    doc.text(`Patient ID : ${patient.id || "-"}`,25,80);

    doc.text(`Age : ${patient.age || "-"}`,110,70);
    doc.text(`Gender : ${patient.gender || "-"}`,110,80);

    // -----------------------------
    // Diagnosis
    // -----------------------------

    doc.setFontSize(18);
    doc.text("AI Prediction",20,118);

    doc.roundedRect(20,126,170,55,3,3);

    doc.setFont("helvetica","bold");
    doc.setFontSize(16);

    doc.text(
        `Diagnosis : ${prediction}`,
        30,
        145
    );

    doc.text(
        `Confidence : ${confidence.toFixed(2)}%`,
        30,
        157
    );

    doc.text(
        `Model : ${model}`,
        30,
        169
    );

    // -----------------------------
    // Date
    // -----------------------------

    doc.setFont("helvetica","normal");

    doc.setFontSize(14);

    doc.text(
        `Date : ${new Date().toLocaleDateString()}`,
        20,
        198
    );

    doc.text(
        `Time : ${new Date().toLocaleTimeString()}`,
        110,
        198
    );

    // -----------------------------
    // Disclaimer
    // -----------------------------

    doc.setFillColor(255,248,220);

    doc.roundedRect(
        20,
        212,
        170,
        55,
        3,
        3,
        "F"
    );

    doc.setFont("helvetica","bold");

    doc.setFontSize(14);

    doc.text(
        "Medical Disclaimer",
        25,
        225
    );

    doc.setFont("helvetica","normal");

    doc.setFontSize(11);

    doc.text(
        [
            "This report is generated using NeuroScan AI.",
            "It is intended for research and educational",
            "purposes only and should not replace",
            "professional clinical diagnosis."
        ],
        25,
        236
    );

    // -----------------------------
    // Footer
    // -----------------------------

    doc.setDrawColor(180);

    doc.line(
        20,
        278,
        190,
        278
    );

    doc.setFontSize(10);

    doc.text(
        "Generated by NeuroScan AI © 2026",
        20,
        285
    );

    doc.save(
        `${patient.name || "Patient"}_EEG_Report.pdf`
    );

  };
  return (
  <div className="min-h-screen bg-slate-100 py-16">
    <div className="max-w-4xl mx-auto bg-white rounded-3xl shadow-2xl overflow-hidden">

      {/* Header */}

      <div className="bg-gradient-to-r from-blue-600 to-indigo-700 text-white p-10 text-center">

        <Brain size={70} className="mx-auto mb-4" />

        <h1 className="text-5xl font-bold">
          EEG Analysis Report
        </h1>

        <p className="mt-3 text-blue-100 text-lg">
          NeuroScan AI - Alzheimer's Detection System
        </p>

      </div>

      <div className="p-10">

        {/* Diagnosis */}

        <div
          className={`rounded-3xl p-10 text-center ${
            healthy
              ? "bg-green-50 border-2 border-green-300"
              : "bg-red-50 border-2 border-red-300"
          }`}
        >

          {healthy ? (
            <CheckCircle2
              size={80}
              className="mx-auto text-green-600"
            />
          ) : (
            <AlertTriangle
              size={80}
              className="mx-auto text-red-600"
            />
          )}

          <h2
            className={`text-5xl font-bold mt-6 ${
              healthy
                ? "text-green-700"
                : "text-red-700"
            }`}
          >
            {prediction}
          </h2>

          <p className="text-gray-500 mt-3 text-lg">
            AI Diagnosis
          </p>

          <div className="mt-6 inline-flex items-center gap-2 bg-blue-100 text-blue-700 px-5 py-3 rounded-full font-bold">

            <Cpu size={20} />

            {model}

          </div>

          <div className="mt-10">

            <h3 className="text-2xl font-bold">
              Confidence
            </h3>

            <div className="w-full bg-gray-200 rounded-full h-6 mt-5 overflow-hidden">

              <div
                className={`h-full ${
                  healthy
                    ? "bg-green-600"
                    : "bg-red-600"
                }`}
                style={{
                  width: `${confidence}%`,
                }}
              />

            </div>

            <h1 className="text-6xl font-black text-blue-600 mt-8">
              {confidence.toFixed(2)}%
            </h1>

            <div className="mt-4 inline-block bg-slate-100 px-4 py-2 rounded-full font-semibold">

              {confidenceLevel}

            </div>
            {/* Model Used */}

            <div className="mt-8 bg-blue-50 border border-blue-200 rounded-2xl p-6">

                <h3 className="text-xl font-bold text-blue-700">
                    Model Used
                </h3>

                <p className="text-3xl font-black mt-2">
                    {model}
                </p>

            </div>

          </div>

        </div>

        {/* Patient Information */}

        <div className="mt-10 bg-slate-50 rounded-3xl p-8">

  <h2 className="text-3xl font-bold flex items-center gap-3 mb-8">
    <User className="text-blue-600" />
    Patient Information
  </h2>

  <div className="grid md:grid-cols-2 gap-6">

    <div className="bg-white rounded-2xl p-5 shadow">
      <p className="text-gray-500">Patient Name</p>
      <h3 className="text-xl font-bold">
        {patient.name || "-"}
      </h3>
    </div>

    <div className="bg-white rounded-2xl p-5 shadow">
      <p className="text-gray-500">Patient ID</p>
      <h3 className="text-xl font-bold">
        {patient.id || "-"}
      </h3>
    </div>

    <div className="bg-white rounded-2xl p-5 shadow">
      <p className="text-gray-500">Age</p>
      <h3 className="text-xl font-bold">
        {patient.age || "-"}
      </h3>
    </div>

    <div className="bg-white rounded-2xl p-5 shadow">
      <p className="text-gray-500">Gender</p>
      <h3 className="text-xl font-bold">
        {patient.gender || "-"}
      </h3>
    </div>

  </div>

</div>

{/* Date & Time */}

<div className="grid md:grid-cols-2 gap-6 mt-10">

  <div className="bg-white rounded-2xl shadow p-6 flex items-center gap-4">

    <Calendar
      className="text-blue-600"
      size={32}
    />

    <div>
      <p className="text-gray-500">
        Date
      </p>

      <h3 className="font-bold text-lg">
        {new Date().toLocaleDateString()}
      </h3>
    </div>

  </div>

  <div className="bg-white rounded-2xl shadow p-6 flex items-center gap-4">

    <Clock
      className="text-blue-600"
      size={32}
    />

    <div>

      <p className="text-gray-500">
        Time
      </p>

      <h3 className="font-bold text-lg">
        {new Date().toLocaleTimeString()}
      </h3>

    </div>

  </div>

</div>

{/* Disclaimer */}

<div className="mt-10 bg-yellow-50 border-l-4 border-yellow-400 rounded-xl p-6">

  <div className="flex items-start gap-3">

    <ShieldCheck
      className="text-yellow-600"
      size={28}
    />

    <div>

      <h3 className="font-bold text-lg">
        AI Medical Disclaimer
      </h3>

      <p className="text-gray-700 mt-2">

        This prediction is generated using machine learning
        models trained on EEG recordings.

        It is intended for research and educational purposes
        only and should not replace professional clinical
        diagnosis.

      </p>

    </div>

  </div>

</div>

{/* Buttons */}

<div className="grid md:grid-cols-2 gap-5 mt-10">

  <button
    onClick={downloadReport}
    className="bg-green-600 hover:bg-green-700 text-white py-5 rounded-2xl font-bold flex justify-center items-center gap-3 transition"
  >

    <Download size={22} />

    Download PDF Report

  </button>

  <button
    onClick={() => navigate("/detect")}
    className="bg-blue-600 hover:bg-blue-700 text-white py-5 rounded-2xl font-bold flex justify-center items-center gap-3 transition"
  >

    <ArrowLeft size={22} />

    Analyze Another EEG

  </button>

</div>

</div>

</div>

</div>

);
}